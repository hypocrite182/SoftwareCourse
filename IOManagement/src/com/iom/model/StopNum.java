package com.iom.model;

public class StopNum {
	private int allStops;
	private int remainStops;
	private int usedStops;
	
	public int getAllStops() {
		return allStops;
	}
	public void setAllStops(int allStops) {
		this.allStops = allStops;
	}
	public int getRemainStops() {
		return remainStops;
	}
	public void setRemainStops(int remainStops) {
		this.remainStops = remainStops;
	}
	public int getUsedStops() {
		return usedStops;
	}
	public void setUsedStops(int usedStops) {
		this.usedStops = usedStops;
	}
	
}
